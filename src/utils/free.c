#include "../../inc/minirt.h"

void	free_matrix(char **matrix)
{
	int	i;

	i = 0;
	while (matrix[i])
	{
		free(matrix[i]);
		i++;
	}
	free(matrix);
}

void	end_program(void)
{
	if (scene()->spheres != NULL)
		free(scene()->spheres);
	exit(0);
}
